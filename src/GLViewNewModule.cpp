void GLViewNewModule::onKeyDown( const SDL_KeyboardEvent& key )
{
   GLView::onKeyDown( key );
   if( key.keysym.sym == SDLK_0 )
      this->setNumPhysicsStepsPerRender( 1 );

   if( key.keysym.sym == SDLK_1 )
   {

      
   }

   if (key.keysym.sym == SDLK_2)
   {
       irrklang::vec3df position(0, 0, 20);
       // start the sound paused:
       irrklang::ISound* snd = sndeng->play3D("../mm/sounds/spaceship.wav", position, false, true);

       if (snd) {
           snd->setMinDistance(1.0f); // a loud sound
           snd->setIsPaused(false); // unpause the sound
       }
   }

   if (key.keysym.sym == SDLK_w)
   {
       this->cam->moveInLookDirection();
   }

   if (key.keysym.sym == SDLK_a)
   {
       this->cam->moveLeft();
   }

   if (key.keysym.sym == SDLK_d)
   {
       this->cam->moveRight();
   }

   if (key.keysym.sym == SDLK_s)
   {
       this->cam->moveOppositeLookDirection();
   }

   if (key.keysym.sym == SDLK_r)        
       sndeng->stopAllSounds();
        
   if (key.keysym.sym == SDLK_4) {
       createAlien();
   }

}


void GLViewNewModule::onKeyUp( const SDL_KeyboardEvent& key )
{
   GLView::onKeyUp( key );
}


